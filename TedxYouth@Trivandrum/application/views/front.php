        <h2 class="title">About TED</h2>
         <p>TED is a nonprofit organization devoted to Ideas Worth Spreading. Started as a conference in California 26 years ago, TED has grown to support those world-changing ideas with many initiatives.
</p><p>At a TED conference, the world's leading thinkers and doers are asked to give the talk of their lives in 18 minutes or less. TED speakers have included Roger Ebert, Sheryl Sandberg, Bill Gates, Elizabeth Gilbert, Benoit Mandelbrot, Philippe Starck, Ngozi Okonjo-Iweala, Brian Greene, Isabel Allende and former UK Prime Minister Gordon Brown. Three major TED events are held each year: The TED Conference takes place every spring in Vancouver, Canada, simultaneous with TEDActive, in Whistler, BC; and the TEDGlobal Conference takes place each summer in Edinburgh, Scotland.
</p><p>On TED.com, talks from TED conferences are shared with the world for free as TED Talks videos. A new TED Talk is posted every weekday. Through the Open Translation Project, TED Talks are subtitled by volunteers worldwide into more than 90 languages. Through our distribution networks, TED Talks are shared on TV, radio, Netflix and many websites.
</p><p>The TEDx initiative grants free licenses to people around the world to organize TED-style events in their communities with TED Talks and live speakers. More than 5,000 TEDx events have been held, and selected talks from these events are also turned into TED Talks videos.
</p><p>	The annual TED Prize grants $1 million to an exceptional individual with a wish to change the world. The TED Fellows program helps world-changing innovators from around the globe to become part of the TED community and, with its help, amplify the impact of their remarkable projects and activities. TED-Ed creates short video lessons by pairing master teachers with animators, for use in classroom instruction or independent learning. 
